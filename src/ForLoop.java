
public class ForLoop {

	public static void main(String[] args) {
	
		System.out.println("----------Numbers from 1 to 10------------");
		
		for(int x=1;x<=10;x++) 
		{
			System.out.println(x);
		}
		
		System.out.println("----------Numbers from 10 to 1------------");
		
		for(int x=10;x>=1;x--) 
		{
			System.out.println(x);
		}

	}

}
